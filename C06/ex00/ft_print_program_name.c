#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	*str;

	i = 0;
	str = argv[0];
	if (argc == 1)
	{
		while (str[i] != '\0')
		{
			write(1, &str[i], 1);
			i++;
		}
		write(1, "\n", 1);
		return (0);
	}
	return (0);
}
